def tidy_name(firstname,secondname):
	#输出整洁的姓名
	name = firstname + "  " + secondname
	return name.title()
print(tidy_name("liefu",'tuoersitai'))

def  dic_name(first_name,second_name):
	#输出字典
	name = {'first_name':first_name,'second_name':second_name}
	return name
dic_name('gaga','lady')















































































































































































































































































































































